 #EJERCICIO 6.- Calcular el área y el perímetro de un Rectángulo
print ( "--------------------Ejercicio_6------------------")

Alto = int(input (" Ingrese un valor para el alto del rectangulo : \n"))
print(Alto)


ancho = int(input (" Ingrese un valor para el ancho  del rectangulo: \n"))
print(ancho)


#perimetro de un rectangulo:   (alto + ancho) * 2
perimetro = (Alto * ancho) * 2
print (f"el valor final del perimetro del rectangulo es :",perimetro  )

#Area de un rectangulo: alto * ancho  
area = (Alto * ancho)
print (f"el valor final del area del rectangulo es :",area )


